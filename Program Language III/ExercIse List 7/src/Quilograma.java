public class Quilograma {

    public static float quilogramaToHectograma( float quilograma ) { return quilograma * 10; }

    public static float quilogramaToDecagrama( float quilograma ) { return quilograma * 100; }

    public static float quilogramaToGramas( float quilograma ) { return quilograma * 1000; }

    public static float quilogramaToDecigrama( float quilograma ) { return quilograma * 10000; }

    public static float quilogramaToCentigrama( float quilograma ) { return quilograma * 100000; }

    public static float quilogramaToMiligrama( float quilograma ) { return quilograma * 1000000; }

    public static float miligramaToQuiligrama( float miligrama ) { return miligrama / 1000000; }

    public static float centigramaToQuilograma( float centigrama ) { return centigrama / 100000; }

    public static float decigramaToQuilograma( float decigrama ) { return decigrama / 10000; }

    public static float gramaToQuilograma( float grama ) { return grama / 1000; }

    public static float decagramaToQuilograma( float decagrama ) { return decagrama / 100; }

    public static float hectagramaToQuilograma( float hectagrama ) { return hectagrama / 10; }

}
