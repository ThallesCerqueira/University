public class Gramas {

    public static float gramaToDecigrama( float grama ) { return grama * 10; }

    public static float gramaToCentigrama( float grama ) { return grama * 100; }

    public static float gramaToMiligrama( float grama ) { return grama * 1000; }

    public static float gramaToDecagrama( float grama ) { return grama / 10; }

    public static float gramaToHectograma( float grama ) { return grama / 100; }

    public static float gramaToQuilograma( float grama ) { return grama / 1000; }

    public static float decigramaToGramas( float decigrama ) { return decigrama / 10; }

    public static float centrigramaToGrama( float centrigrama ) { return centrigrama / 100; }

    public static float miligramaToGrama( float miligrama ) { return miligrama / 1000; }

    public static float decagramaToGrama( float decagrama ) { return decagrama * 10; }

    public static float hectogramaToGrama( float hectograma ) { return hectograma * 100; }

    public static float quilogramaToGrama( float quilograma ) { return quilograma * 1000; }

}
